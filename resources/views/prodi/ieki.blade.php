      <p>Visi, Misi dan Tujuan Prodi IEKI
Visi
Menjadi program studi pelopor dan unggul (leading and outstanding) dalam penyelenggaraan pendidikan dan pengembangan ilmu ekonomi, keuangan dan bisnis Islam di Indonesia yang terekognisi secara internasional pada tahun 2025.
Misi
Menyelenggarakan pelayanan pendidikan dan pembelajaran yang berkualitas berstandar nasional dan internasional dengan memadukan perkembangan ilmu, pengetahuan, dan teknologi terkini (IPTEK) serta dinamika masyarakat, untuk menghasilkan lulusan yang memiliki kompetensi unggul di bidang ilmu ekonomi, keuangan, dan bisnis Islam;
Melakukan penelitian untuk mengembangkan ilmu ekonomi, keuangan, dan bisnis Islam untuk pemenuhan kebutuhan pengembangan ilmu dan masyarakat yang berorientasi kepada publikasi ilmiah nasional dan internasional bereputasi;
Melaksanakan pengabdian kepada masyarakat, dalam rangka menyebarluaskan dan penerapan ilmu ekonomi, keuangan dan bisnis Islam;
Membangun jejaring kerjasama dengan masyarakat ilmu dan/ atau masyarakat profesi bidang ekonomi, keuangan dan bisnis Islam baik  nasional maupun internasional;
Melaksanakan tata kelola PS IEKI yang mampu menciptakan atmosfer dan kultur akademik berkualitas standar nasional dan internasional
 

Tujuan Prodi IEKI :

Menghasilkan sarjana ekonomi Islam yang memiliki keunggulan kompetitif dan komparatif global dalam mengembangkan dan mengaplikasikan ilmu pengetahuan dan teknologi bidang ekonomi Islam, keuangan Islam, bisnis Islam dan filantropi Islam;
Menghasilkan berbagai karya akademik, khususnya hasil penelitian untuk mengembangkan dan mengaplikasikan ilmu ekonomi, keuangan Islam, bisnis Islam, dan filantropi Islam;
Menghasilkan lulusan dengan kemampuan intelektual/ilmuwan yang islami dan mampu memasuki dan/atau menciptakan lapangan kerja, serta mampu mengembangkan diri menjadi sumber daya insani profesional pada bidang ekonomi, keuangan Islam, bisnis Islam dan filantropi Islam;
Menghasilkan jejaring kerjasama secara regional, nasional dan internasional dan berbagai layanan profesional pada bidang ilmu ekonomi Islam, Keuangan Islam, bisnis Islam, dan filantropi Islam.
